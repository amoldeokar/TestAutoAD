package MyPackage;

public class WhileDemo {

	public static void main(String[] args) {

		
		while(true)
		{
			int x=11;
			System.out.println("X " + x);
			x++;
			if (x>20); break;
		}
		
		do{
			int y=5;
			System.out.println("this will print atleast once");
	
			if (y>20); break;
		}while(true);
		
		
	}

}
